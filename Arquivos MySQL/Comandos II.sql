CREATE TABLE CURSOS(
	IDCURSO INT AUTO_INCREMENT,
	NOME VARCHAR (25) NOT NULL,
	HORAS INTEGER (3) NOT NULL,
	VALOR INTEGER (10),
	PRIMARY KEY (IDCURSO)
);


CREATE PROCEDURE CAD_CURSO(NULL ,NOME VARCHAR(25), HORAS INTEGER(3), VALOR INTEGER (10,2))
BEGIN

	INSERT INTO CURSOS VALUES(NULL, NOME, HORAS, VALOR);

END$

